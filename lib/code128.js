function getCode(mode, ch) {
  var i = -1;

  switch(mode.toUpperCase()) {
  case 'A': i = CODEAMAP.indexOf(ch); break;
  case 'B': i = CODEBMAP.indexOf(ch); break;
  case 'C': i = CODECMAP.indexOf(ch); break;
  }

  return i < 0 ? [] : BARMAP[i];
}

